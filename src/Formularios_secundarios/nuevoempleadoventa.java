                        cst.setString(6,direccionEmpleadoV.getText());
                        cst.setString(7,correoEmpleadoV.getText());
                        cst.setInt(8,Integer.parseInt(docEmpleadoV.getText()));
                        cst.execute();
                        
                        JOptionPane.showMessageDialog(null,"Nuevo empleado ingresado");
                        
