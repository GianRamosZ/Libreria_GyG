        Connection con;
        con = conexion.conectar();
        
        String nombre,apellido,direccion,correo,nombreusuario;
        int documento, celular;
        
        nombre = nombreEmpleadoV.getText();
        apellido = apellidoEmpleadoV.getText();
        direccion = direccionEmpleadoV.getText();
        correo = correoEmpleadoV.getText();
        nombreusuario = nombreUsuarioV.getText();
        documento = Integer.parseInt(docEmpleadoV.getText());
        celular = Integer.parseInt(celularEmpleadoV.getText());
        
